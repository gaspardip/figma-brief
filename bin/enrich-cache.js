#!/usr/bin/env node

// Layer 2: LLM-based enrichment of cached MCP data.
// Reads raw designContext from cache, extracts structured data via claude CLI,
// writes enriched result back to cache. No MCP calls, no rate limits.
//
// Usage:
//   node bin/enrich-cache.js                    # enrich all cached designs
//   node bin/enrich-cache.js --only=my-design   # enrich matching designs
//   node bin/enrich-cache.js --force             # re-enrich even if already done

import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const CACHE_DIR = path.join(__dirname, "..", "autoresearch", "cache");

const EXTRACTION_PROMPT = `You are analyzing Figma design context code (React+Tailwind generated by Figma MCP).
Extract ALL of the following as a JSON object. Be thorough — capture everything visible.

1. "uiText": All user-visible text content (button labels, headings, body copy, placeholders, tooltips). Exclude code artifacts like className, data attributes, variable names.
2. "components": All React component names used (e.g., Button, Modal, Toast). Just the names, no props.
3. "behaviors": Any behavioral descriptions found in annotations, comments, or long text nodes. These describe what happens on interaction (hover, click, drag, state changes).
4. "layout": High-level layout description — what sections exist, how they're arranged (sidebar + main, stacked cards, modal overlay, etc).
5. "states": Different UI states visible (default, hover, active, disabled, error, loading, etc).
6. "accessibility": Any accessibility-related content (aria labels, focus indicators, screen reader text).

Return ONLY valid JSON, no markdown fences:
{
  "uiText": ["string", ...],
  "components": ["string", ...],
  "behaviors": ["string", ...],
  "layout": "string",
  "states": ["string", ...],
  "accessibility": ["string", ...]
}`;

async function callClaude(content) {
  const input = `${EXTRACTION_PROMPT}\n\nDesign context code:\n\`\`\`\n${content.slice(0, 60000)}\n\`\`\``;

  try {
    const { spawn } = await import("node:child_process");

    return await new Promise((resolve, reject) => {
      const proc = spawn("claude", ["--print", "--model", "sonnet"], {
        stdio: ["pipe", "pipe", "pipe"],
        timeout: 120000,
      });

      let stdout = "";
      let stderr = "";

      proc.stdout.on("data", (d) => {
        stdout += d;
      });
      proc.stderr.on("data", (d) => {
        stderr += d;
      });

      proc.on("close", (code) => {
        if (code !== 0) {
          reject(new Error(stderr.slice(0, 200) || `exit code ${code}`));
          return;
        }

        const jsonMatch = stdout.match(/\{[\s\S]*\}/);

        if (jsonMatch) {
          try {
            resolve(JSON.parse(jsonMatch[0]));
          } catch {
            reject(new Error("Failed to parse JSON from Claude output"));
          }
        } else {
          reject(new Error("No JSON found in Claude output"));
        }
      });

      proc.stdin.write(input);
      proc.stdin.end();
    });
  } catch (error) {
    console.error(
      `  Claude extraction failed: ${error.message.slice(0, 200)}`,
    );
    return null;
  }
}

async function enrichDesign(cacheFile, force) {
  const cached = JSON.parse(await fs.readFile(cacheFile, "utf8"));
  const name = path.basename(cacheFile, ".json");

  // Skip if already enriched (unless --force)
  if (cached.enriched && !force) {
    console.log(`  ${name}: already enriched, skipping (use --force to redo)`);
    return false;
  }

  // Use designContext (code) if available, fall back to featureMetadata (XML)
  const content = cached.designContext ?? cached.featureMetadata;

  if (!content || typeof content !== "string" || content.length < 100) {
    console.log(`  ${name}: no content to enrich (${content?.length ?? 0} chars)`);
    return false;
  }

  const source = cached.designContext ? "designContext" : "featureMetadata";
  console.log(`  ${name}: extracting from ${content.length} chars of ${source}...`);

  const extraction = await callClaude(content);

  if (!extraction) {
    console.log(`  ${name}: extraction failed`);
    return false;
  }

  // Write enrichment back to cache
  cached.enriched = {
    extractedAt: new Date().toISOString(),
    ...extraction,
  };

  await fs.writeFile(cacheFile, `${JSON.stringify(cached, null, 2)}\n`, "utf8");

  const stats = [
    `${extraction.uiText?.length ?? 0} texts`,
    `${extraction.components?.length ?? 0} components`,
    `${extraction.behaviors?.length ?? 0} behaviors`,
    `${extraction.states?.length ?? 0} states`,
  ].join(", ");

  console.log(`  ${name}: enriched (${stats})`);
  return true;
}

async function main() {
  const force = process.argv.includes("--force");
  const onlyArg = process.argv.find((a) => a.startsWith("--only="));
  const onlyTarget = onlyArg?.split("=")[1]?.toLowerCase() ?? null;

  const files = await fs.readdir(CACHE_DIR);
  const cacheFiles = files
    .filter((f) => f.endsWith(".json"))
    .filter((f) => !onlyTarget || f.toLowerCase().includes(onlyTarget));

  if (cacheFiles.length === 0) {
    console.log("No cache files found. Run bin/autoresearch.js --refresh first.");
    return;
  }

  console.log(`Enriching ${cacheFiles.length} cached designs...\n`);

  let enriched = 0;

  for (const file of cacheFiles) {
    const ok = await enrichDesign(path.join(CACHE_DIR, file), force);

    if (ok) {
      enriched++;
    }
  }

  console.log(`\nDone. Enriched ${enriched}/${cacheFiles.length} designs.`);
}

main().catch((error) => {
  console.error(error.message);
  process.exitCode = 1;
});
